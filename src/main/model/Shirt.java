package model;

// Represents a shirt to be worn by a Person
public class Shirt {
    private String name;

    // EFFECTS: Creates a shirt with a name
    public Shirt(String name) {
        this.name = name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getName() {
        return name;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

}
